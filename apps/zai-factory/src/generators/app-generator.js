import { logger } from '../logger.js';
import { safeWrite } from '../fs-safe.js';
import { validateContent } from '../safety.js';
import path from 'node:path';

export const sanitizeName = (name) => {
    const sanitized = name.replace(/[^a-zA-Z0-9-]/g, '');
    if (!sanitized || sanitized !== name) {
        throw new Error(`Invalid app name: ${name}`);
    }
    return sanitized;
};

export const generate = async (config) => {
    const sanitizedName = sanitizeName(config.name);
    
    const pkgData = {
        name: sanitizedName,
        version: '0.1.0',
        description: `ZeaZ Platform app: ${sanitizedName}`
    };

    if (!validateContent(pkgData)) {
        throw new Error('Sensitive data detected in generation');
    }

    logger.info(`Generating app: ${sanitizedName}`);
    const appPath = path.join('apps', sanitizedName);
    
    // Simple scaffold logic
    safeWrite(path.join(appPath, 'package.json'), pkgData);
    safeWrite(path.join(appPath, 'README.md'), `# ${sanitizedName}\n\nApp generated by ZAI Factory.`);
    
    logger.info(`App generated at ${appPath}`);
    return { path: appPath };
};
